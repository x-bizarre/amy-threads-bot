// ─────────────────────────────────────────────────────────────────────────────
// КОНФИГ БРЕНДА — единственное место, где задаётся, "от чьего имени" работает бот.
//
// Весь остальной код тянет идентичность отсюда. Чтобы поднять бота под свой
// проект, достаточно поменять значения в этом файле (или задать переменные
// окружения с теми же именами — env имеет приоритет).
//
// Голос бренда, контент-стратегия и кастдев-вопросы НЕ здесь — они вынесены
// в текстовые шаблоны в папке `prompts/` (см. prompts/README.md).
// ─────────────────────────────────────────────────────────────────────────────

export const BRAND = {
  // Название продукта/бренда. Подставляется в промпты и UI.
  name: process.env.BRAND_NAME ?? 'Acme',

  // Хэндл аккаунта в Threads (без @). Например: 'acme.brand'
  handle: process.env.THREADS_USERNAME ?? 'your-brand-handle',

  // Сайт продукта. Бот даёт ссылку только если человек прямо спросил "где это".
  site: process.env.BRAND_SITE ?? 'example.com',

  // Имя основателя для постов "от первого лица" (опционально).
  // Если у тебя нет founder-голоса — оставь пустым.
  founder: process.env.BRAND_FOUNDER ?? '',

  // Одна строка: что это за продукт. Используется в коротких промптах.
  oneLiner:
    process.env.BRAND_ONE_LINER ??
    'короткое описание твоего продукта одной фразой',
} as const;

// Удобные геттеры с @ для текстов
export const handleAt = () => `@${BRAND.handle}`;
