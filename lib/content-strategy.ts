// Распределение целей по неделе. Используется в post-generator.ts когда Amy
// решает какой пост сгенерить следующим.
export interface MixRatio {
  subscribe: number;
  discovery: number;
  brand: number;
}

export const DEFAULT_MIX: MixRatio = {
  subscribe: 0.65,
  discovery: 0.30,
  brand: 0.05,
};

// Какую цель выбрать для следующего поста, зная распределение последних N постов.
// Возвращает ту цель, которая «отстаёт» от целевого процента.
export function pickNextGoal(recentGoals: ContentGoal[], mix: MixRatio = DEFAULT_MIX): ContentGoal {
  if (recentGoals.length === 0) return 'subscribe';
  const total = recentGoals.length;
  const counts: Record<ContentGoal, number> = { subscribe: 0, discovery: 0, brand: 0 };
  for (const g of recentGoals) counts[g]++;

  // Считаем deficit (целевая доля - фактическая) для каждой цели
  const deficits: Array<[ContentGoal, number]> = [
    ['subscribe', mix.subscribe - counts.subscribe / total],
    ['discovery', mix.discovery - counts.discovery / total],
    ['brand', mix.brand - counts.brand / total],
  ];
  deficits.sort((a, b) => b[1] - a[1]);
  return deficits[0][0];
}
