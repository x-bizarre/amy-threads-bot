// Голос бренда и формат ответа на комментарии.
//
// ВАЖНО: сам текст голоса бренда здесь больше НЕ хранится — он лежит в
// prompts/brand-voice.md и читается оттуда. Правь только тот markdown.
// Здесь остаётся неизменная «техническая» часть: формат JSON-ответа.

import { loadTemplate } from './load-template';

// Неизменная часть промпта — формат ответа. Добавляется к голосу бренда.
const RESPONSE_FORMAT = `
## Задача
Тебе дают:
- Текст поста (контекст, что человек прочитал).
- Всё, что пользователь написал в ветке под этим постом.

Твой ответ (СТРОГО JSON):
1. **comment_ru** — перевод комментария пользователя на русский (короткий, естественный — не подстрочник). Нужен, чтобы автор быстро понял(а), что написали.
2. **draft_ru** — черновик ответа на **русском языке**. По умолчанию заканчивается уточняющим вопросом. На него автор скажет "ок" или попросит поправить.
3. **recommendation** — "publish" (предлагаем ответить) или "skip" (спам / негатив / ничего осмысленного).
4. **skip_reason** — одной фразой, если recommendation=skip.

Когда автор одобрит draft_ru, ты отдельным шагом переведёшь его на естественный английский и опубликуешь.

Пиши чётко, без "Конечно!" и "С радостью!". Сразу к делу.
`.trim();

// Собирает системный промпт: голос бренда (из markdown) + формат ответа.
// Асинхронный, потому что голос читается из репозитория.
export async function getBrandVoicePrompt(): Promise<string> {
  const voice = await loadTemplate('brand-voice.md');
  return `${voice}\n\n${RESPONSE_FORMAT}`;
}

// Формат ответа от LLM — строгий JSON, чтобы парсить без ошибок
export interface DraftResult {
  comment_ru: string; // перевод комментария на русский — показываем автору
  recommendation: 'publish' | 'skip';
  skip_reason?: string;
  draft_ru?: string; // черновик ответа на русском, если recommendation=publish
}
